package com.kk.readExcel.case2.console;

/**
 * excel处理的异常
 * 
 * @author liujun
 * @date 2016年4月1日
 * @verion 0.0.1
 */
public class JavaExcelProcException extends Exception
{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public JavaExcelProcException()
	{
		super();
	}

	public JavaExcelProcException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public JavaExcelProcException(String message)
	{
		super(message);
	}

	public JavaExcelProcException(Throwable cause)
	{
		super(cause);
	}

}
